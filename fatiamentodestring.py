print (frase[0:12])
'''do zero até o 12 pois sempre deixa um passando nessa forma de mostrar a string no fatiamento'''
print (frase[0:12:2])
'''aqui voce vai fazer o mesmo do de cima porem vai pular de dois em dois caracteres'''
print (frase[:5])
'''ele sempre vai começar do começo se n botar nada'''
print (frase[5:])
